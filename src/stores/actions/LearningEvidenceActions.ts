import { createNotification } from "../../helpers/Notification";
import { client } from '../graphql';
import { MUTATION_CHANGE_ACTIVE_LEARNING_EVIDENCE, MUTATION_CREATE_LEARNING_EVIDENCE, MUTATION_DELETE_LEARNING_EVIDENCE, MUTATION_UPDATE_LEARNING_EVIDENCE } from '../graphql/LearningEvidence/LearningEvidenceMutations';
import { QUERY_GET_ALL_LEARNING_EVIDENCE, QUERY_GET_LEARNING_EVIDENCE, QUERY_GET_DROPDOWNS_LEARNING_EVIDENCE } from '../graphql/LearningEvidence/LearningEvidenceQueries';


export const getListAllLearningEvidence = (schoolId:string, learningId: string) => {
  return async (dispatch: any) => {
    try {
      let listData = {};
      await client
        .query({
          query: QUERY_GET_ALL_LEARNING_EVIDENCE,
          variables:{
            schoolId,
            learningId,
          },
        })
        .then((result: any) => {
          listData = result.data.data.edges;
        });
      return listData;
    } catch (error) {
      createNotification('error', 'error', '');
      return error;
    }
  };
};

