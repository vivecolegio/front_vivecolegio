import { createNotification } from "../../helpers/Notification";
import { client } from '../graphql';
import { MUTATION_CHANGE_ACTIVE_SYNC_OFFLINE, MUTATION_CREATE_SYNC_OFFLINE, MUTATION_DELETE_SYNC_OFFLINE, MUTATION_UPDATE_SYNC_OFFLINE } from '../graphql/SyncOffline/SyncOfflineMutations';
import { QUERY_GET_SYNC_OFFLINE,  QUERY_GET_ALL_SYNC_OFFLINE, QUERY_GET_DROPDOWNS_SYNC_OFFLINE } from '../graphql/SyncOffline/SyncOfflineQueries';

export const getListAllSyncOffline = (schoolId:string, schoolYearId:string, fullAccess: boolean ) => {
  return async (dispatch: any) => {
    try {
      let listData = {};
      await client
        .query({
          query: QUERY_GET_ALL_SYNC_OFFLINE,
          variables:{
            schoolId,
            schoolYearId,
            allData: fullAccess ? fullAccess : false,
          },
        })
        .then((result: any) => {
          listData = result.data.data.edges;
        });
      return listData;
    } catch (error) {
      createNotification('error', 'error', '');
      return error;
    }
  };
};

