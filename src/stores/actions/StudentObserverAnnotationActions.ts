import { createNotification } from "../../helpers/Notification";
import { client } from '../graphql';
import { MUTATION_CHANGE_ACTIVE_STUDENT_OBSERVER_ANNOTATION, MUTATION_CREATE_STUDENT_OBSERVER_ANNOTATION, MUTATION_DELETE_STUDENT_OBSERVER_ANNOTATION, MUTATION_UPDATE_STUDENT_OBSERVER_ANNOTATION } from "../graphql/StudentObserverAnnotation/StudentObserverAnnotationMutations";
import { QUERY_GET_ALL_STUDENT_OBSERVER_ANNOTATION, QUERY_GET_DROPDOWNS_STUDENT_OBSERVER_ANNOTATION, QUERY_GET_STUDENT_OBSERVER_ANNOTATION } from "../graphql/StudentObserverAnnotation/StudentObserverAnnotationQueries";

export const getListAllStudentObserverAnnotation = (courseId:string, studentId:string) => {
  return async (dispatch: any) => {
    try {
      let listData = {};
      await client
        .query({
          query: QUERY_GET_ALL_STUDENT_OBSERVER_ANNOTATION,
          variables:{
            courseId,
            studentId
          }
        })
        .then((result: any) => {
          listData = result.data.data.edges;
        });
      return listData;
    } catch (error) {
      createNotification('error', 'error', '');
      return error;
    }
  };
};

