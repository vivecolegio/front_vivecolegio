import { createNotification } from "../../../helpers/Notification";
import { client } from '../../graphql';
import { MUTATION_CHANGE_ACTIVE_GRADE, MUTATION_CREATE_GRADE, MUTATION_DELETE_GRADE, MUTATION_UPDATE_GRADE } from '../../graphql/GeneralAcademic/Grade/GradeMutations';
import { QUERY_GET_ALL_GRADE, QUERY_GET_DROPDOWNS_GRADE, QUERY_GET_GRADE } from '../../graphql/GeneralAcademic/Grade/GradeQueries';


export const getListAllGrade = () => {
  return async (dispatch: any) => {
    try {
      let listData = {};
      await client
        .query({
          query: QUERY_GET_ALL_GRADE,
        })
        .then((result: any) => {
          listData = result.data.data.edges;
        });
      return listData;
    } catch (error) {
      createNotification('error', 'error', '');
      return error;
    }
  };
};

