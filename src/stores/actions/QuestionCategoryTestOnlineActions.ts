import { createNotification } from "../../helpers/Notification";
import { client } from '../graphql';
import { MUTATION_CHANGE_ACTIVE_QUESTION_CATEGORY_TEST_ONLINE, MUTATION_CREATE_QUESTION_CATEGORY_TEST_ONLINE, MUTATION_DELETE_QUESTION_CATEGORY_TEST_ONLINE, MUTATION_UPDATE_QUESTION_CATEGORY_TEST_ONLINE } from '../graphql/QuestionCategoryTestOnline/QuestionCategoryTestOnlineMutations';
import { QUERY_GET_ALL_QUESTION_CATEGORY_TEST_ONLINE, QUERY_GET_QUESTION_CATEGORY_TEST_ONLINE } from '../graphql/QuestionCategoryTestOnline/QuestionCategoryTestOnlineQueries';


export const getListAllQuestionCategoryTestOnline = (campusId: string) => {
  return async (dispatch: any) => {
    try {
      let listData = {};
      await client
        .query({
          query: QUERY_GET_ALL_QUESTION_CATEGORY_TEST_ONLINE,
          variables: {
            campusId,
          },
        })
        .then((result: any) => {
          listData = result.data.data.edges;
        });
      return listData;
    } catch (error) {
      createNotification('error', 'error', '');
      return error;
    }
  };
};

